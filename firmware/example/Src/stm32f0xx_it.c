/**
* @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
*/
void USART1_IRQHandler(void)
{
  
  // SDP - receive one byte at a time  
  if(LL_USART_IsActiveFlag_RXNE(USART1) && LL_USART_IsEnabledIT_RXNE(USART1))
  {
    // RXNE flag will be cleared by reading of RDR register (done in call)
    sdp_receive_data(&uc_node); // receive byte and store it in internal buffer
  }
  
  // check for raming, error, overrun error or noise flag (FE=1 or ORE=1 or NF=1
  if(LL_USART_IsEnabledIT_ERROR(USART1) && 
      (LL_USART_IsActiveFlag_FE(USART1) || LL_USART_IsActiveFlag_ORE(USART1) || LL_USART_IsActiveFlag_NE(USART1))){
    
    UART_ErrorCallback(USART1);
  }

}

/**
* @brief UART error handler
*/
void UART_ErrorCallback(USART_TypeDef *huart){
  // check for framing error, overrun error or noise flag (FE=1 or ORE=1 or NF=1
  if(LL_USART_IsActiveFlag_FE(huart)){
    LL_USART_ClearFlag_FE(huart);
  }

  if(LL_USART_IsActiveFlag_ORE(huart)){
    LL_USART_ClearFlag_ORE(huart);
  }

  if(LL_USART_IsActiveFlag_NE(huart)){
    LL_USART_ClearFlag_NE(huart);
  }
  
  // clear sdp buffer and other data
  if(huart == USART1){
    sdp_reset_node(&uc_node);    
  }
  else{
    // add your other nodes handler here
  }
  
}
